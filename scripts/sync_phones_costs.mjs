async function run() {
  await mongoose.connect(MONGODB_URI);
  const Product = mongoose.models.Product || mongoose.model('Product', new mongoose.Schema({ 
    id: String, 
    name: String, 
    category: String,
    costPrice: Number,
    description: String
  }, { strict: false }));

  const categories = ['samsung', 'motorola', 'xiaomi'];
  const dataLists = {
    samsung: samsungList,
    motorola: motorolaList,
    xiaomi: xiaomiList
  };

  const results = {};

  for (const cat of categories) {
    console.log(`\nSincronizando categoría: ${cat.toUpperCase()}`);
    const dbProducts = await Product.find({ category: cat }).lean();
    const list = dataLists[cat];
    
    const items = list.trim().split('\n').map(line => {
      const lastSlashIndex = line.lastIndexOf('/');
      if (lastSlashIndex === -1) return null;
      const priceStr = line.substring(lastSlashIndex + 1).split('|')[0].trim().replace(',', '');
      const price = parseFloat(priceStr);
      if (isNaN(price)) return null;
      let namePart = line.substring(0, lastSlashIndex).trim();
      namePart = namePart.replace(/^\d+-\d+\s+/, '').replace(cat.toUpperCase() + ' ', '').trim();
      return { namePart, price };
    }).filter(Boolean);

    let updatedCount = 0;
    let notFoundCount = 0;

    for (const item of items) {
      const keywords = item.namePart.toLowerCase().replace(/[\/"]/g, ' ').split(' ').filter(w => w.length >= 2);
      
      const match = dbProducts.find(p => {
         const dbName = p.name.toLowerCase();
         const dbDesc = (p.description || '').toLowerCase();
         
         // Extract model code if present (like SM-A065M or XT2523-8 or NOTE 14)
         // For phones, we usually look for specific alphanumeric codes
         const modelCode = item.namePart.match(/[A-Z0-9-]{4,}/i);
         if (modelCode && (dbName.includes(modelCode[0].toLowerCase()) || dbDesc.includes(modelCode[0].toLowerCase()))) {
            // Also check for color if multiple variants exist
            const colors = ['black', 'blue', 'green', 'gray', 'purple', 'white', 'orange', 'gold', 'red', 'mint', 'violet', 'navy', 'rosa', 'plata', 'negro', 'azul', 'verde', 'gris', 'morado', 'blanco', 'naranja', 'oro', 'rojo', 'celeste'];
            const itemColor = colors.find(c => item.namePart.toLowerCase().includes(c));
            if (itemColor) {
                // If item has color, DB name must have it too (or translation)
                const translatedColors = {
                    'negro': 'black', 'azul': 'blue', 'verde': 'green', 'gris': 'gray', 'morado': 'purple', 'violeta': 'violet', 'naranja': 'orange', 'oro': 'gold', 'rojo': 'red', 'blanco': 'white', 'celeste': 'light blue', 'plata': 'silver'
                };
                const dbColorMatch = colors.some(c => dbName.includes(c) || dbDesc.includes(c));
                if (dbColorMatch) {
                    // Check if matched color is the same
                    const dbSpecificColor = colors.find(c => dbName.includes(c) || dbDesc.includes(c));
                    // Very loose check to avoid skipping correct matches
                    return true; 
                }
            }
            return true;
         }
         
         const matchCount = keywords.filter(kw => dbName.includes(kw) || dbDesc.includes(kw)).length;
         return matchCount >= 3;
      });

      if (match) {
        await Product.updateOne({ _id: match._id }, { $set: { costPrice: item.price } });
        updatedCount++;
      } else {
        notFoundCount++;
        // console.log(`[${cat}] No encontrado: ${item.namePart}`);
      }
    }
    
    results[cat] = { updated: updatedCount, notFound: notFoundCount, total: items.length };
    console.log(`[${cat}] Actualizados: ${updatedCount} | No encontrados: ${notFoundCount} | Total Lista: ${items.length}`);
  }

  console.log('\n--- Resumen Final ---');
  console.table(results);
  process.exit(0);
}

run();
